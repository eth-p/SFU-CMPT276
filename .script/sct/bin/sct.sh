HERE="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
ROOT="$(dirname "$(dirname "$(dirname "${HERE}")")")"
TEMP="${ROOT}/.tmp"

# ----------------------------------------------------------------------------------------------------------------------
# Setup:
# ----------------------------------------------------------------------------------------------------------------------
[[ -d "${TEMP}"           ]] || mkdir -p "$TEMP"              || exit $?
[[ -f "${TEMP}/sct-setup" ]] || source "${HERE}/sct-setup.sh" || exit $?

# ----------------------------------------------------------------------------------------------------------------------
# Main:
# ----------------------------------------------------------------------------------------------------------------------
node "${HERE}/sct.js" "$@"
exit $?
