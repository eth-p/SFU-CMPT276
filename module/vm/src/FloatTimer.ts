import Timer from './Timer';
// ---------------------------------------------------------------------------------------------------------------------

/**
 * A floating-point number timer.
 */
class FloatTimer extends Timer {
	// -------------------------------------------------------------------------------------------------------------
	// | Constructor:                                                                                              |
	// -------------------------------------------------------------------------------------------------------------

	/**
	 * Creates a new timer.
	 *
	 * @param cpuHz The main clock speed.
	 * @param timerHz The timer clock speed.
	 */
	public constructor(cpuHz: number, timerHz: number) {
		super(cpuHz, timerHz);
	}
	// -------------------------------------------------------------------------------------------------------------
	// | Methods: Override                                                                                         |
	// -------------------------------------------------------------------------------------------------------------

	/**
	 * @inheritDoc
	 * @override
	 */
	public ascend(): void {
		this.value += this.ratio;
	}

	/**
	 * @inheritDoc
	 * @override
	 */
	public descend(): void {
		this.value -= this.ratio;
	}
}

// ---------------------------------------------------------------------------------------------------------------------
export default FloatTimer;
export {FloatTimer};
